  public void editContact() {
    click(By.xpath("//img[@alt='Edit']"));
  }

  public void submitContactEdition() {
    click(By.name("update"));
  }

  public void deleteContact() {
    click(By.xpath("//div[@id='content']/form[2]/input[2]"));
  }

